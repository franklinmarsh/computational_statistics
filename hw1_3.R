G.Prime <- function(x) {
  #returns the value of the function G.prime evaluated at x.
  #
  # Args:
  #   x: the value at which to evaluate the function
  #
  # Returns:
  #   g'(x)  
  return((1 + 1/x - log(x)) / ((1+x) ^ 2))
}

